 class Pattern70
{
 public static void main(String args[])
 {
  int n=5;
  for(int i=1;i<=(n*2)-1;i++)//1<=9//2<=9//3<=9//4<=9//5<=9//6<=9//7<=9
   {
      if(i<=n)//1<=5//2<=5//3<=5//4<=5//5<=5//6<=5!//7<=5!
        {
          for(int j=i;j<=n;j++)//1<=5,2<=5,3<=5,4<=5,5<=5//2<=5,3<=5,4<=5,5<=5//3<=5,4<=5,5<=5//4<=5,5<=5//5<=5
                System.out.print("*");
}
              else
                {
                   for(int j=i;j>=n;j--)//6>=5,5>=5//7>=5,6>=5,5>=5
                  System.out.print("*");
}
                   System.out.println();
}
}
}